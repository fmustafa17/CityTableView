import UIKit

extension UIColor {
    @nonobjc class var battleshipGrey: UIColor {
      return UIColor(red: 112.0 / 255.0, green: 120.0 / 255.0, blue: 138.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var paleGreyThree: UIColor {
      return UIColor(red: 240.0 / 255.0, green: 247.0 / 255.0, blue: 250.0 / 255.0, alpha: 1.0)
    }
}
